WITH CLIENTI_SOLO_DENTISTA( CLIENTE, DID) AS
(SELECT P.Cliente, MIN(I.DID)
 FROM PREVENTIVI P, INTERVENTI I, DENTISTI D
 WHERE D.DID = I.DID
 GROUP BY P.Cliente
 HAVING count(DISTINCT I.DID) = 1
),
CONTEGGI (DID, NumInterventi) AS 
(SELECT I.DID, COUNT(I.CodI)
 FROM PREVENTIVI P, INTERVENTI I, CLIENTI_SOLO_DENTISTA C
 WHERE P.PID = I.DID
 AND P.Cliente = C.Cliente
 AND I.DID = C.DID
 GROUP BY I.DID
)
SELECT D.DID, D.Nome, COALESCE(C.NumINterventi, 0 ) AS NumInterventi
FROM DENTISTI D
LEFT JOIN CONTEGGI C ON D.DID=C.DID;

 