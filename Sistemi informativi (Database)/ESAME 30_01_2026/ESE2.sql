SELECT I1.codI AS CodI1, I2.CodI AS CodI2
FROM INTERVENTI I1, INTERVENTI I2 
WHERE I1.PID = I2.PID 
AND I1.DID = I2.DID
AND I1.NumDente = I2.NumDente
AND I1.NumDente IS NOT NULL 
AND I1.DATA < I2.DATA
AND NOT EXISTS ( SELECT *
			   FROM INTERVENTI I3
			   WHERE I3.PID = I1.PID
			   AND I3.NumDente=I1.NumDente
			   AND I1.DATA < I3.DATA
			   AND I3.DATA < I2.DATA );
			
			  
			  
